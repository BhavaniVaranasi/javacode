package basics;

public class Calling_Multi_StaticMethod 
{
	static void method1()
	{
		System.out.println("Method_one");
	}
	static void method2()
	{
		System.out.println("Method_two");
	}
	static void method3()
	{
		System.out.println("Method_three");
	}
	static void method4()
	{
		System.out.println("Method_four");
	}
	static void method5()
	{
		System.out.println("Method_five");
	}
public static void main(String[] args)
{
	method1();
	method2();
	method3();
	method4();
	method5();
}

}
